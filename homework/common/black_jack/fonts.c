/**
 * @brief
 *  Returns the image information for a given ASCII character
 *  Make sure to set the values of all of the image_t fields.
 *
 *  Set the image_type be set to IMAGE_TYPE_FONT
 *  Set the bitmap width based on the character selected
 *  Set the bitmap height based on the character selected
 *  The fcolor should be set to a green foreground color
 *  The bcolor should be set to a black background color
 * @param c
 * Ascii Character
 * @param image
 * Image information for the provided character
 * @return true
 * true is returned if a valid ascii character is provided
 * @return false
 * false is returned if the character is a non-printable ascii char
 */
bool font_get_image(char c, image_t *image)
{

	uint8_t index = c - '!';
	if (index <=31 || index >= 127){
		return false;
	}
	uint16_t bitmap_offset = bebasNeue_28ptDescriptors[index].offset;
	uint8_t *bitmap_addr = (uint8_t *) & bebasNeue_28ptBitmaps[bitmap_offset];
	image->bitmap = bitmap_addr;
	image->width = bebasNeue_28ptDescriptors[index].width;
	image->image_type = IMAGE_TYPE_FONT;
	image->height = bebasNeue_28ptDescriptors[index].height;
	image->fcolor = "#008000";
	image->bcolor = "#000000";
	return true;
}